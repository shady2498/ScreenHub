<h1>Gemini Man</h1>
<p>Gemini Man is a 2019 American action thriller film[3] directed by Ang Lee. Starring Will Smith, Mary Elizabeth Winstead, Clive Owen and Benedict Wong, and written by David Benioff, Billy Ray and Darren Lemke, the film follows a former hitman who is targeted by a younger clone of himself while on the run from the government.
Originally conceived in 1997 by screenwriter Lemke, the film went through development hell for nearly 20 years. Several directors, including Tony Scott, Curtis Hanson, and Joe Carnahan, were attached at some point and numerous actors, including Harrison Ford, Mel Gibson and Sean Connery, were set to star. In 2016, Skydance Media purchased the rights to the screenplay (which had been through several rewrites) from Disney and in October 2017, Ang Lee signed on to direct for Skydance with Paramount handling the distribution rights. Filming took place from February through May 2018.
Gemini Man was released on October 1, 2019 and was theatrically released in the United States by Paramount Pictures on October 11, 2019, in standard 3D, Dolby Cinema, 4DX, IMAX 3D, and ScreenX Formats, as well as in HFR (high frame rate) "3D+" on select screens (including 16 Dolby Cinema[4] and 47 IMAX 3D with Laser screens worldwide).[5] The film received generally negative reviews from critics for its script and plot, although the performances were praised. The de-aging of Smith and the high frame-rate of 120 FPS also drew a mixed response, with some critics praising them as technical achievements, while others felt they were unconvincing.[6][7] It grossed $173 million against a production budget of $138 million, making it a box-office bomb, with projected losses as high as $111 million for Paramount.
