<h1>Black Lightning</h1>
<p>Black Lightning is an American superhero television drama series, developed by Salim Akil, that premiered on The CW on January 16, 2018. It is based on the character of the same name, created by Tony Isabella and Trevor Von Eeden, featured in publications of DC Comics. Cress Williams stars as the titular character alongside China Anne McClain, Nafessa Williams, Christine Adams, Marvin Jones III, Damon Gupton, James Remar, and Jordan Calloway. The series sees the retired Black Lightning return to his life as a superhero and follows the effects of his vigilante activity on his professional and family life.

